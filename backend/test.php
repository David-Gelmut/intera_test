/**
 * Задача 1: Two Sum (Сумма двух чисел) — Классика на проверку O(1)
 * Условие: Дан массив целых чисел nums и целевое число target.
 * Нужно вернуть индексы двух чисел, которые в сумме дают target.
 * Каждое число можно использовать только один раз.
*/

function twoSum(array $nums, int $target): array {
    $seen = []; // Хэш-таблица: [значение => индекс]

    foreach ($nums as $index => $value) {
        $complement = $target - $value;

        // В PHP поиск по ключу в ассоциативном массиве работает за O(1)
        if (isset($seen[$complement])) {
            return [$seen[$complement], $index];
        }

        // Запоминаем текущее число и его индекс
        $seen[$value] = $index;
    }

    return [];
}

/**
 *  Задача 2: Валидация скобок (Паттерн «Стек»)
 * Условие: Дана строка, состоящая только из символов (), {}, [].
 * Нужно определить, является ли строка валидной (каждая открытая скобка должна быть закрыта скобкой того же типа в правильном порядке).
 * Правильное решение через Стек (Array как Stack в PHP):
 * В PHP обычный массив идеально выполняет роль стека с помощью функций array_push() и array_pop().
 */


function isValidBrackets(string $s): bool {
    $length = strlen($s);
    if ($length % 2 !== 0) return false; // Нечетное количество символов не может быть валидным

    $map = [
        ')' => '(',
        '}' => '{',
        ']' => '[',
    ];
    $stack = [];

    for ($i = 0; $i < $length; $i++) {
        $char = $s[$i];

        // Если это закрывающая скобка
        if (isset($map[$char])) {
            // Достаем элемент с вершины стека (или берем заглушку, если стек пуст)
            $topElement = !empty($stack) ? array_pop($stack) : '#';

            // Если скобки не совпали — строка невалидна
            if ($topElement !== $map[$char]) {
                return false;
            }
        } else {
            // Если это открывающая скобка — кладем в стек
            $stack[] = $char; // Быстрее, чем array_push($stack, $char)
        }
    }

    // Если стек в конце пустой — все скобки закрылись правильно
    return empty($stack);
}






/**
 * Задача 3: Слияние интервалов (Паттерн «Сортировка»)
 * Условие: Дан массив интервалов, где каждый интервал — это [start, end]
 * (например, бронирование переговорки: [[1,3], [2,6], [8,10], [15,18]]).
 * Нужно объединить все перекрывающиеся интервалы.
 *
 * Решение (Сортировка в PHP под капотом):
 * Сначала интервалы нужно отсортировать по времени начала (start).
 * В PHP для этого используется функция usort(), которая под капотом реализует алгоритм Quicksort
 * (Быстрая сортировка) с временной сложностью \(O(N \log N)\).
 */

function mergeIntervals(array $intervals): array {
    if (empty($intervals)) return [];

    // 1. Сортируем интервалы по первому элементу [start]
    usort($intervals, function($a, $b) {
        return $a[0] <=> $b[0]; // Используем Spaceship оператор PHP 7+
    });

    $merged = [$intervals[0]];
print_r($merged);
    // 2. Идем по отсортированным интервалам и склеиваем их
    $count = count($intervals);
    for ($i = 1; $i < $count; $i++) {
        $current = $intervals[$i];
        $lastMergedIndex = count($merged) - 1;

        // Если начало текущего интервала МЕНЬШЕ или РАВНО концу последнего склеенного
        if ($current[0] <= $merged[$lastMergedIndex][1]) {
            // Расширяем конец последнего склеенного интервала
            $merged[$lastMergedIndex][1] = max($merged[$lastMergedIndex][1], $current[1]);
        } else {
            // Перекрытия нет, просто добавляем новый интервал в результат
            $merged[] = $current;
        }
    }

    return $merged;
}

// Пример работы:
// Вход:  [[1,3], [2,6], [8,10], [15,18]]
// Выход: [[1,6], [8,10], [15,18]]

print_r(mergeIntervals([[1,3], [2,6], [8,10], [15,18]])) ;
